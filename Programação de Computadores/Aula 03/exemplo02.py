#Exemplo de calculadora simples
#entrada de dados
n1 = float(input("Digite um valor: "))
n2 = float(input('Digite outro valor: '))
#processamento e a saída
print('Soma =', (n1+n2))
print('Subtração =', (n1-n2))
print('Multiplicação =', (n1*n2))
print('Divisão =', (n1/n2))
print('Resto da divisão =',(n1%n2))